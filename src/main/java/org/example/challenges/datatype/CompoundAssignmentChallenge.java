package src.main.java.org.example.challenges.datatype;

/*
initialize an int variable, named result to the value of 10 and use the compound assignment operator to perform
minus, multiplication and division with your own value and print the result
 */
public class CompoundAssignmentChallenge {

    public static void main(String[] args) {
        int result = 10;

        result -= 15;

        System.out.println(result);

        result *= -5;

        System.out.println(result);

        result /= 5;

        System.out.println(result);

    }

}
